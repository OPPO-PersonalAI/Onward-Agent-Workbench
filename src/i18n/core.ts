const translations: Record<AppLocale, LocaleTranslations> = {
  en,
  'zh-CN': zhCN
}

export function isAppLocale(value: unknown): value is AppLocale {
  return value === 'en' || value === 'zh-CN'
}

export function normalizeLocale(value: unknown): AppLocale {
  return isAppLocale(value) ? value : DEFAULT_LOCALE
}

function formatMessage(template: string, params?: TranslationParams): string {
  if (!params) return template
  return template.replace(/\{\{(.*?)\}\}/g, (_, rawKey: string) => {
    const key = rawKey.trim()
    const value = params[key]
    return value === null || value === undefined ? '' : String(value)
  })
}

export function translate(locale: AppLocale, key: TranslationKey, params?: TranslationParams): string {
  const message = translations[locale][key] ?? en[key]
  return formatMessage(message, params)
}

export function createTranslator(locale: AppLocale) {
  return (key: TranslationKey, params?: TranslationParams) => translate(locale, key, params)
}
